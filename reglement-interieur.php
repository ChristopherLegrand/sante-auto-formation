        <h1>Règlement intérieur pour les participants en formation de Sante Auto Formation</h1>
    <h2>Conformément Aux Articles L6352-3 et L6352-4 et R6352-1 à R6352-15 Du Code Du Travail</h2>

    <h3>Préambule</h3>
<p>SANTE AUTO FORMATION, est un organisme de
formation dont la déclaration d'activité est enregistrée,
auprès du préfet de la région Aquitaine, sous le numéro
72 33 06204 33.<br/><br/>
Son siège social est situé au 27, chemin des Arneys à
Castelnau de Médoc (33480);<br/><br/>
Le règlement intérieur est établi conformément aux
dispositions législatives et réglementaires régissant la
formation professionnelle continue (article L.6352-3,
L.6352-4, L.6352-5 et R.6352-1 et suivants du code du
travail). Il s’applique à toutes les personnes
participantes à une action de formation organisée par
SANTE AUTO FORMATION qui doivent s'y conformer
sans restriction ni réserve.<br/><br/>
Un exemplaire du présent règlement est remis avant
l’inscription définitive avec la convention/contrat de
formation et à chaque stagiaire inscrit accompagné de
sa convocation.<br/><br/>
SANTE AUTO FORMATION, est ci-après désigné
« l’organisme de formation ».</p>
<h4>Article 1 – Dispositions générales</h4>
<p>Le règlement définit les règles d’hygiène et de sécurité́,
les règles générales et permanentes relatives à la
discipline ainsi que la nature et l’échelle des sanctions
pouvant être prises vis-à-vis des stagiaires qui y
contreviennent et les garanties procédurales
applicables lorsqu’une sanction est envisagée.</p>
<h4>Article 2 – Champ d’application</h4>
<p>Sont concernés par l’application du présent règlement,
l’ensemble des stagiaires inscrits et présents à une
formation dispensée par SANTE AUTO FORMATION,
pour toute la durée de la formation suivie et tant que le
stagiaire est présent sur le lieu du stage.<br/><br/>
Les formations se tiennent dans des locaux extérieurs
à SANTE AUTO FORMATION Les dispositions du
présent règlement sont applicables dans l’ensemble
des locaux où sont dispensés des formations par
SANTE AUTO FORMATION.</p>
<h3>Section 1 : Règles d’hygiène et de sécurité</h3>
<h4>Article 3 - Principes généraux</h4>
<p>La prévention des risques d’accidents et de maladies
est impérative et exige de chacun le respect :<br/>
- des prescriptions applicables en matière d’hygiène et
de sécurité́ sur les lieux de formation,<br/>
- de toute consigne imposée dans l’établissement ou
sont dispensées les formations de l’organisme de
formation notamment de l’usage des matériels mis à
disposition.</p>
<h4>Article 4 – Hygiène et sécurité</h4>
<p>Chaque stagiaire doit ainsi veiller à sa sécurité́
personnelle et à celle des autres en respectant, en
fonction de sa formation, les consignes générales et
particulières en matière d’hygiène et de sécurité́.
S’il constate un dysfonctionnement du système de
sécurité́, il en avertit immédiatement la direction de
l’organisme de formation. Le non-respect de ces
consignes expose la personne à des sanctions
disciplinaires.<br/>
Lorsque les formations se déroulent dans une
entreprise ou un établissement déjà doté d’un
règlement intérieur, les mesures de sécurité et
d’hygiène applicables aux stagiaires sont celles de ce
dernier règlement, en application de l’article R. 922-1
du code du travail.</p>
<h4>Article 5 - Principes généraux</h4>
<p>Les locaux dans lesquels sont réalisées les formations
dispensées par SANTE AUTO FORMATION sont
totalement non fumeurs en application de l’article
R.355-28-1 du code de la santé publique.<br/>
Il est interdit aux stagiaires d’apporter des boissons
alcoolisées dans la salle de formation. Il est en outre
interdit de pénétrer dans les salles de formation en état
d’ivresse ou sous l’emprise de drogue.<br/><br/>

Il est également interdit aux stagiaires de prendre leur
repas dans les salles où sont organisés les stages.</p>
<h4>Article 6 : Consignes d’incendie</h4>
<p>Les consignes d’incendie et notamment un plan de
localisation des extincteurs et des issues de secours
sont affichés dans les locaux de l’établissement ou se
déroule la formation. Le stagiaire doit en prendre
connaissance. En cas d’alerte, le stagiaire doit cesser
toute activité́ de formation et suivre dans le calme les
instructions du représentant habilité de l’établissement
ou se déroule la formation ou des services de secours.</p>
<h4>Article 7 : Accident</h4>
<p>Tout accident ou incident survenu à l’occasion ou en
cours de formation doit être immédiatement déclaré par
le stagiaire accidenté ou les personnes témoins de
l’accident, au responsable de l’organisme.
Conformément à l’article R.962-1 du code du travail,
l’accident survenu au stagiaire pendant qu’il se trouve
sur le lieu de formation ou pendant qu’il s’y rend ou en
revient fait l’objet d’une déclaration par le responsable
de l’organisme auprès de la caisse de sécurité sociale.</p>
<h4>Article 8 : Tenue et comportement</h4>
<p>Les stagiaires sont invités à se présenter au lieu de
formation en tenue décente et à avoir un comportement
garantissant le respect des règles élémentaires de
savoir vivre, de savoir être en collectivité́.
Des prescriptions vestimentaires peuvent être
demandées au stagiaire pour les formations
nécessitant une tenue spécifique.</p>
<h3>Section 2 : Discipline générale</h3>
<h4>Article 9 – Assiduité du stagiaire en formation</h4>
<h4>Article 9.1. - horaires de formation</h4>
<p>Les horaires de stage sont fixés à l’avance par SANTE
AUTO FORMATION et portés à la connaissance des
stagiaires lors de la remise du programme du stage.
Les stagiaires sont tenus de respecter ces horaires de
formation. En cas d’absence ou de retard à une
formation, les stagiaires sont tenus d’informer le
responsable de l’organisme de formation.
SANTE AUTO FORMATION, se réserve le droit de
modifier les horaires de stage en prévenant à l’avance
ses stagiaires sauf cas de force majeure.</p>
<h4>Article 9.2. - Absences, retards ou départs
anticipés</h4>
<p>En cas d’absence, de retard ou de départ avant l’horaire
prévu, les stagiaires doivent avertir l’organisme de
formation et s’en justifier. L’organisme de formation
informe immédiatement le financeur (employeur,
administration, OPCO, Région, Pôle emploi, CPF,...) de
cet événement.<br/><br/>
Tout événement non justifié par des circonstances
particulières constitue une faute passible de sanctions
disciplinaires. De plus, conformément à l’article R6341-
45 du Code du travail, le stagiaire – dont la
rémunération est prise en charge par les pouvoirs
publics – s’expose à une retenue sur sa rémunération
de stage proportionnelle à la durée de l’absence.</p>
<h4>Article 9.3. - Formalisme attaché au suivi de la
formation</h4>
<p>Le stagiaire est tenu de renseigner la feuille
d’émargement au fur et à mesure du déroulement de
l’action. Il peut lui être demandé de réaliser un bilan de
la formation.<br/><br/>
A l’issue de l’action de formation, le stagiaire se voit
remettre un questionnaire d’évaluation qu’il doit remplir
et remettre au formateur.<br/>
Une attestation de fin de stage sera ensuite délivrée
aux participants
Le stagiaire remet, dans les meilleurs délais, à
l’organisme de formation les documents qu’il doit
renseigner en tant que prestataire (demande de
rémunération ou de prise en charges des frais liés à la
formation ; attestations d’inscription ou d’entrée en
stage...).</p>
<h4>Article 10 : Accès au lieu de formation</h4>
<p>Sauf autorisation expresse de SANTE AUTO
FORMATION les stagiaires ayant accès à la salle de
formation pour suivre leur formation ne peuvent faciliter
l’introduction de tierces personnes à l’organisme.</p>
<h4>Article 11 : Usage du matériel</h4>
<p>Chaque stagiaire est tenu d’utiliser le matériel
conformément à son usage pour la réalisation de la
formation. Le matériel fourni au stagiaire pendant la
formation doit être conservé en bon état.
A la fin de la formation, les stagiaires ont l’obligation de
restituer le matériel et les documents mis à leur
disposition par SANTE AUTO FORMATION, à
l’exception des documents pédagogiques distribués
aux stagiaires pendant la formation.
Il est rigoureusement interdit, sauf dérogation expresse,
d’enregistrer ou de filmer les sessions de formation. Les
documents pédagogiques remis pendant les formations
sont protégés par les droits d’auteur. Leur reproduction,
sans l’autorisation, est formellement interdite. Ces
documents ne peuvent être réutilisés que dans un
objectif personnel.</p>
<h4>Article 12 : Responsabilité de l’organisme de
formation</h4>
<p>L’organisme de formation décline toute responsabilité
en cas de perte, vol ou détérioration des objets
personnels de toute nature apportés par les stagiaires
sur le lieu de formation.</p>
<h4>Article 13 : Respect de la confidentialité des
données stagiaires</h4>
<p>SANTE AUTO FORMATON peut recueillir directement après
du stagiaire ou indirectement via des tiers des données à
caractère personnel et/ou professionnel. Conformément au
principe de minimisation lié à la réglementation RGPD, nous
ne collectons que les données nécessaires au regard des
finalités pour lesquelles elles sont traitées. Tous les
formateurs de SANTE AUTO FORMATION, s’engagent à
garder confidentielles les informations personnelles et
professionnelles des stagiaires qui seraient portées à sa
connaissance.</p>
<h3>Section 3 : Mesures disciplinaires</h3>
<h4>Article 14 – Sanctions disciplinaires</h4>
<p>Tout manquement du stagiaire à l’une des prescriptions
du présent règlement intérieur pourra faire l’objet d’une
sanction prononcée par le responsable de l’organisme
de formation ou son représentant. Tout agissement
considèré comme fautif pourra, en fonction de sa nature
et de sa gravité, faire l’objet de l’une ou l’autre des
sanctions suivantes :<br/>
- rappel à l’ordre ;<br/>
- avertissement écrit par le directeur de l’organisme de
formation ou par son représentant ;<br/>
- blâme ;<br/>
- exclusion temporaire de la formation ;<br/>
- exclusion définitive de la formation.<br/>
Les amendes ou autres sanctions pécuniaires sont
interdites. Le responsable de l’organisme de formation
ou son représentant informe de la sanction prise :<br/>
- l’employeur du salarié stagiaire ou l’administration de
l’agent stagiaire ;<br/>
- et/ou le financeur du stage.</p>
<h3>Section 4 : Entrée en vigueur</h3>
<h4>Article 15 – Entrée en vigueur</h4>
<p>Ce règlement intérieur est applicable pour les stagiaires
en formation à SANTE AUTO FORMATION. Un
exemplaire du présent règlement sera remis aux
stagiaires qui suivent une formation à SANTE AUTO
FORMATION. Le présent règlement entre en vigueur à
compter du 25 mai 2018.</p>

